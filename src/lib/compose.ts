/* Чистая логика композера экрана «Новая мысль» (ComposeScreen) — вынесена из
   компонента, чтобы покрыть тестами без рендера. UI-состояние записи живёт в
   recorder.ts; здесь только текстовая часть. */

/** Длина текста, после которой композер раскрывается в две строки (текст сверху,
   кнопки снизу) — паттерн Meta AI / Gemini. Порог НИЗКИЙ: в компактном поле кнопки
   слева (+/🎤) и справа (отправка) съедают ширину, текст начинает переноситься уже
   ~с 16 символов — раскрываем ДО этого, иначе он некрасиво теснится между кнопок.
   Перенос строки тоже раскрывает. Char-based (не по измеренной высоте) — измерение
   дало бы осцилляцию: раскрылось→шире→влезло в строку→схлопнулось→снова узко. */
export const EXPAND_CHAR_THRESHOLD = 16;

/** Можно ли отправить текстовую мысль: есть непустой текст и сейчас не идёт сохранение. */
export function canSend(text: string, saving: boolean): boolean {
  return !saving && text.trim().length > 0;
}

/** Раскрыть ли композер в две строки. Компакт (одна строка) пока текст короткий и
   однострочный; раскрытие — при переносе строки или длине сверх порога. */
export function shouldExpandComposer(text: string): boolean {
  if (text.includes("\n")) return true;
  return text.trim().length > EXPAND_CHAR_THRESHOLD;
}
